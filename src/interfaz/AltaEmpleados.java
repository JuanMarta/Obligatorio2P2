    private void jListEmpleadosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListEmpleadosValueChanged
        Empleado e = jListEmpleados.getSelectedValue();

        jTextCedula.setText(e.getCedula());
        jTextNombre.setText(e.getNombre());
        jTextCelular.setText(e.getCelular());
        jTextSalario.setText(String.valueOf(e.getSalarioMensual()));
        jTextAreaCurriculum.setText(e.getCv());
        jComboManager.setSelectedItem(e.getManager());
        jComboAreas.setSelectedItem(e.getArea());

        revalidate();
        repaint();
    }//GEN-LAST:event_jListEmpleadosValueChanged

    private void jBtnAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAltaActionPerformed
        // Datos
        String cedula = jTextCedula.getText();

        if (sistema.unicidadPersona(new Empleado(cedula))) {
            String nombre = jTextNombre.getText();
            String celular = jTextCelular.getText();
            Double salario = Double.parseDouble(jTextSalario.getText());
            String curriculum = jTextAreaCurriculum.getText();
            Manager manager = (Manager) jComboManager.getSelectedItem();
            Area area = (Area) jComboAreas.getSelectedItem();
            
            Empleado empleado = new Empleado(nombre, cedula, celular, salario, curriculum, manager, area);
            sistema.agregarEmpleado(empleado);
            JOptionPane.showMessageDialog(this, "Empleado creado correctamente", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "La cedula ingresada ya existe", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBtnAltaActionPerformed

    private void inicio() {
        // Arregla el tama√±o de los jText, sin esto usa el layout y se hacen muy chicos
        jTextCedula.setPreferredSize(new Dimension(64, 22));
        jTextNombre.setPreferredSize(new Dimension(64, 22));
        jTextCelular.setPreferredSize(new Dimension(64, 22));
        jTextSalario.setPreferredSize(new Dimension(64, 22));

        // Cargo lista con empleados
        DefaultListModel<Empleado> modelo = new DefaultListModel<>();

        for (Empleado e : sistema.empleadosOrdenadosPorSalario()) {
            modelo.addElement(e);
        }

        jListEmpleados.setModel(modelo);

        // Carga los combos de manager y √°rea
        for (Manager m : sistema.getListaManagers()) {
            jComboManager.addItem(m);
        }

        for (Area a : sistema.getListaAreas()) {
            jComboAreas.addItem(a);
        }
    }

